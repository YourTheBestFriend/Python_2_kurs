#-------------------------------
# range()

# Range(Stop)
peremen = range(5)
for i in peremen:
	print(i, end = ",")
print('\n')

# Range(Start, Stop)
for i in range(1, 7):
   print(i, end = ", ")

# Range(Start, Stop[, Step])
for i in range(1, 7, 3):
   print(i, end = ", ")
#-------------------------------

