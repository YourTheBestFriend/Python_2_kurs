# Методы find, rfind, replace, count, split, join, index, try-except

#-------------------------------
# find() 
# find()  строка в которой будет произведен поиск
print('холод'.find('о', 2, 5)) # в срезе [2:5] 'холод' ищем первое совпадение с 'о' 
print('холод'.find('о')) # в строке 'холод' ищем первое совпадение с 'о'
print('холод'.find('у')) # в строке 'холод' ищем первое совпадение с 'у'

# rfind 
print('холод'.rfind('о', 0, 3)) # в срезе [0:3] 'холод' ищем первое совпадение с 'о' 
print('холод'.rfind('о')) # в строке 'холод' ищем первое совпадение с 'о'
print('холод'.rfind('у')) # в строке 'холод' ищем первое совпадение с 'у'
